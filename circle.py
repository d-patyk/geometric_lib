import math


def area(r):
    '''Returns circle area

    Args:
        a (number): circle radius

    Returns:
        number: circle area
    '''
    return math.pi * r * r


def perimeter(r):
    '''Returns circle perimeter

    Args:
        a (number): circle radius

    Returns:
        number: circle perimeter
    '''
    return 2 * math.pi * r
