def area(a, b):
    '''Returns rectangle area

    Args:
        a (number): rectangle first side
        b (number): rectangle second side

    Returns:
        number: rectangle area
    '''
    return a * b


def perimeter(a, b):
    '''Returns rectangle perimeter

    Args:
        a (number): rectangle first side
        b (number): rectangle second side

    Returns:
        number: rectangle perimeter
    '''
    return (a + b) * 2
