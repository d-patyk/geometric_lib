def area(a, h):
    '''Returns triangle area

    Args:
        a (number): triangle base side
        h (number): triangle altitude

    Returns:
        number: triangle area
    '''
    return a * h / 2


def perimeter(a, b, c):
    '''Returns triangle perimeter

    Args:
        a (number): triangle first side
        b (number): triangle second side
        b (number): triangle third side

    Returns:
        number: triangle perimeter
    '''
    return a + b + c
